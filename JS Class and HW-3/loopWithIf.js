for (i=0; i<=10; i++){
    if (i==3) {
        document.writeln(`3 is a lucky number` + "<br>");
    } else if (i==5) {
        document.writeln(`5 is a lucky number` + "<br>");
    } else if (i==7) {
        document.writeln(`7 is a lucky number` + "<br>");
    } else  {
        document.writeln(`Number ${i}` + "<br>");
    }
}